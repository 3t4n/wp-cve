namespace VitePos_Lite\Libs;

require_once __DIR__ . '/../../appsbd_lite/v1/libs/class-appinput.php';
require_once __DIR__ . '/../../appsbd_lite/v1/libs/class-wp-loader.php';
require_once __DIR__ . '/class-vitepos-addons.php';

use Appsbd_Lite\V1\libs\WP_Loader;

if ( ! class_exists( __NAMESPACE__ . '\Vitepos_Loader' ) ) {
	/**
	 * Class Vitepos_Loader
	 *
	 * @package VitePos\Libs
	 */
	class Vitepos_Loader extends WP_Loader {

		/**
		 * The set details is generated by appsbd
		 *
		 * @return mixed|void
		 */
		public function set_details() {
			$this->plugin_file     = 'vitepos-lite/vitepos-lite.php';
			$this->pro_plugin_file = 'vitepos/vitepos.php';
		}

		/**
		 * The load before ready is generated by appsbd
		 *
		 * @param bool $result Its a result.
		 *
		 * @return bool
		 */
		public function load_before_ready( $result ) {
			new Vitepos_Addons( $result, $this->base_file, $this );

			return parent::load_before_ready( $result );
		}
	}
}
