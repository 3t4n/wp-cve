    public static function as_data_entitites(){
      $entities = [];
      foreach(self::$carriers as $carrier){
        $entities[$carrier['carrier_name']] = [
          'id' => $carrier['carrier_name'],
          'name' => $carrier['Carrier']
        ];
      }
      return $entities;
    }

    public static function get_my_carriers($my_carriers){
      $ret = [];
      foreach(self::$carriers as $carrier){
        if(in_array($carrier['carrier_name'], $my_carriers)){
          $ret[] = $carrier;
        }
      }
      return $ret;
    }
