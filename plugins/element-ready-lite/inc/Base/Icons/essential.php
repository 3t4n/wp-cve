class Element_Ready_Add_Essential_Icons {
    
    public function __construct() { 
		add_filter( 'elementor/icons_manager/additional_tabs', [ $this, 'element_ready_elementor_essential_setup' ] );
	}
    
    public function element_ready_enqueue_essential(){
        wp_enqueue_style( 'essential', ELEMENT_READY_ROOT_ICON . 'essential/essential.css', array(), '1.0.0' );
    }

	public function element_ready_elementor_essential_setup( $tabs = array()){

		$new_icons = element_ready_essential_sets();

		$tabs['essential'] = array(
			'name'          => 'essential',
			'label'         => esc_html__( 'Essential Icons', 'element-ready-lite' ),
			'labelIcon'     => 'essential-folder-14',
			'prefix'        => '',
			'displayPrefix' => 'essential',
			'url'           => esc_url(ELEMENT_READY_ROOT_ICON . 'essential/essential.css'),
			'icons'         => $new_icons,
			'ver'           => '1.0.0',
		);
		return $tabs;
	}

}
new Element_Ready_Add_Essential_Icons();