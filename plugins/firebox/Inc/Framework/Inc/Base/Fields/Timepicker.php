use FPFramework\Libs\Registry;

class Timepicker extends Datepicker
{
	/**
	 * Set specific field options
	 * 
	 * @param   array  $options
	 * 
	 * @return  void
	 */
	protected function setFieldOptions($options)
	{
		$options = new Registry($options);

		// remove the Type
		unset($this->options['type']);

		$this->field_options = [
			'input_class' => ['fpf-timepicker-item'],
			'type' => 'Datepicker'
		];
	}
}