use FPFramework\Base\Field;
use FPFramework\Libs\Registry;

class Spacer extends Field
{
	/**
	 * Set specific field options
	 * 
	 * @param   array  $options
	 * 
	 * @return  void
	 */
	protected function setFieldOptions($options)
	{
		$options = new Registry($options);

		$this->field_options = [
			'render_group' => false,
			'top' => $options->get('top', 20),
			'bottom' => $options->get('bottom', 20),
			'hr' => $options->get('hr', true)
		];
	}
}