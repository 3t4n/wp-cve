/**
 * Generate a random color
 *
 * since 1.0.2
 *
 * @return string hex color code.
 */
function eaccounting_get_random_color() {
	return '#' . str_pad( dechex( random_int( 0, 0xFFFFFF ) ), 6, '0', STR_PAD_LEFT );
}

