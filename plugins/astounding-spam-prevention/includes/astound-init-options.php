function astound_reset_options() {
	$options=astound_get_defaults();
	update_option('astound_options',$options);
}
function astound_init_options() {
	$options=get_option('astound_options');
	$ansa=astound_get_defaults();
	// to keep from getting option creep we then set the options from opts back into the ansa
	// had to do this to get rid of obsolete or mistaken options.
	if (empty($options)||!is_array($options)) {
		$options=array();
	}
	// get rid of old keys
	foreach($options as $key=>$val) {
		if (!array_key_exists($key,$ansa)) {
			unset($options[$key]);
		} 
	}
	// go throufh the ansas and make sure it is exists in options. 
	foreach($ansa as $key=>$val) {
		if (!array_key_exists($key,$options)) {
			$options[$key]=$val;
		}
	}
	$options['version']=ASTOUND_VERSION;
	// check the numeric varables for numericness - user can enter anything
	update_option('astound_options',$options);
	return $options;
}







