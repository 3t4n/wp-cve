<p>User Reaction Counts are the counts located inside of your badges.</p>
<p>This feature was designed to give you control to set your user counts to any number.</p>
<p><strong>How will my counts read?</strong><br>
    All counts over 999 will read as 1K and will go up in increments of 100 and will be shown as 1.1K, 1.2K etc. Counts
    over 999K will start at 1M and so on and so forth.</p>
<p><strong>How will my visitors see the actual counts if over 1K?</strong><br>
    When your visitor clicks on any reaction, the actual count number will fly up in the air and fade away as an
    animation.</p>
