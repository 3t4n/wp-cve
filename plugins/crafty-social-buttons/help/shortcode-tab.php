<p><strong>Shortcodes</strong></p>

<p>To include <em>Share Buttons</em> and <em>Link Buttons</em> in individual pages or posts where they wouldn't otherwise appear, you can include these shortcodes in your content:</p>
<ul>
	<li><code>[csbshare]</code></li>
    <li><code>[csblink]</code></li>
</ul>

<p>The buttons will be output using the settings you have configured on this page.</p>

<p>The <code>[csbshare]</code> shortcode can be configured with additional parameters.  Please see the documentation site
   at <a href="http://sarahhenderson.github.io/Crafty-Social-Buttons/#shortcodes">http://sarahhenderson.github.io/Crafty-Social-Buttons/#shortcodes</a> for more information.</p>

<p>To prevent <em>Share Buttons</em> being added to individual pages or posts where they would normally appear, you can include this shortcode in your content:</p>
<ul>
   <li><code>[csbnone]</code></strong></li>
</ul>

<p>This could be used if you want the buttons to be automatically added to most pages, but want to hide them on some specific pages (such as registration pages, shopping cart pages).</p>

