<p><strong>Adding new icons</strong></p>

<p>Including a new set of icons is pretty easy!  The requirements are:</p>
<ol>
	<li>Your images are <strong>.png</strong> files</li>
    <li>You use the same filenames as the existing image sets
	    (which are pretty straightforward - e.g. facebook is facebook.png, ravelry is ravelry.png)</li>
    <li>Your images are between 24x24px or 64x64px in size. You can set the size in the options page.</li>
</ol>

<p>If you've got all that sorted, you just need to create a new folder to store your icons.</p>

<p>The plugin will look for your custom icon folders in <code>wp-content/uploads/crafty-social-buttons/buttons</code>.</p>
<p>For instance, if your custom icons are in a folder called <code>MyAwesomeIcons</code>, the plugin will look for the
	Facebook icon at <code>wp-content/uploads/crafty-social-buttons/buttons/MyAwesomeIcons/facebook.png</code>.</p>
<p>It is best if you don't have any spaces or punctuation in the folder name.</p>

<p>Now if you refresh this options page, you will see your folder name as one of the options to choose from!</p>
