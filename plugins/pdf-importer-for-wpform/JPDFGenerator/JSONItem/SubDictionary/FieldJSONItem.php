<?php


namespace rnpdfimporter\JPDFGenerator\JSONItem\SubDictionary;


use Exception;
use rnpdfimporter\core\Integration\Processors\Entry\EntryItems\ListEntryItem\EntryItemBase;
use rnpdfimporter\JPDFGenerator\JSONItem\DictionaryObjectItem;
use rnpdfimporter\JPDFGenerator\JSONItem\IndirectObjectJsonItem;

abstract class FieldJSONItem extends DictionaryObjectItem
{

    /** @var IndirectObjectJsonItem */
    public $Parent;

    public function InternalGetText()
    {
        return parent::InternalGetText(); // TODO: Change the autogenerated stub
    }

    public function GetId(){
        return $this->Parent->GetObjectNumber();
    }

    public function GetFieldSettings(){
        foreach($this->Generator->Options->FieldSettings as $setting)
        {
            if($setting->Id==$this->Parent->GetObjectNumber())
                return $setting;
        }
    }
    public abstract function GetType();

    /**
     * @param $formField EntryItemBase
     */
    public abstract function SetFieldValue($formField);


}