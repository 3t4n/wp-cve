<p>
<b>Advanced Option: Forward Query String to Destination URL</b>
</p>

<p>
Check this box to forward the URL&#039;s query string to the destination URL.
</p>

<p>
Let&#039;s say you have a link on your site at the URL <code>http://www.yoursite.com/link-slug</code> that takes the visitor to <code>http://www.example.com/</code>
</p>

<p>
With this box checked, a visitor to <code>http://www.yoursite.com/link-slug?a=1&b=2&c=3</code> would be taken to <code>http://www.example.com/?a=1&b=2&c=3</code>
</p>

