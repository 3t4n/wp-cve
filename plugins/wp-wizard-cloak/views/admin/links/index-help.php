From this page, you can manage the Links you have created with WP Wizard Cloak. To delete a Link, mouse over it and  click <i>Trash</i>. This only moves the Link to the Trash - it does not delete it permanently. To delete a Link permanently, open the Trash, mouse over the Link, and click <i>Delete Permanently</i>.

