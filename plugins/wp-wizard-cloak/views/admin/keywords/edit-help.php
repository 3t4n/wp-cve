<b>Advanced Option: Pass Post/Page ID as Variable in Query String</b>

<p>
	Choose this option and the ID of the Post or Page where the click originates will be passed as the value of the specified variable in the query string. If you are linking to a WP Wizard Cloak-created link or another one of your own links, this feature allows you to determine specifically where traffic is coming from.
</p>

<p>
	For example, you have setup an Auto-Linked Keyword Grouping to link <code>widgets</code> to <code>http://www.widgets.com/</code>, and you have checked the <i>Pass Post/Page ID as...</i> box, and you have left the variable name as the default <code>postpageid</code>. If you have a page on your site with the keyword "widgets" somewhere in it and the ID of that page is 6, and someone clicks on one of the <code>widgets</code> keywords on that page, they will be taken to <code>http://www.widgets.com/?postpageid=6</code>.
</p>

<p>
	Find Post/Page IDs: <a target='_blank' href='http://www.google.com/search?q=how+to+find+wordpress+post+ids'>Google Search for "how to find wordpress post ids"</a> <small>(offsite)</small>
</p>



